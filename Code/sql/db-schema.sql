CREATE FUNCTION archive_usrd_name() RETURNS trigger
    LANGUAGE plpgsql
    AS $$
BEGIN
  insert into usrh_name(usr_id, name) values(OLD.usr_id, OLD.name);
  return NEW;
END;
$$;

CREATE FUNCTION to_iso8601_str(timestamp with time zone) RETURNS character varying
    LANGUAGE sql IMMUTABLE
    AS $_$
  select to_char($1 at time zone 'UTC', 'YYYY-MM-DD"T"HH24:MI:SS"Z"')
$_$;

CREATE FUNCTION usr_login_stats_update() RETURNS trigger
    LANGUAGE plpgsql
    AS $$
BEGIN

    update usr
    set login_count   = login_count + 1
       ,last_login_at = NEW.time
       ,last_login_ip = NEW.ip -------------- changed to accept NULL here
    where id = NEW.usr_id;

    RETURN NULL;
END;
$$;

