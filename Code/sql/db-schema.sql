CREATE TABLE event (
    project_id bigint NOT NULL,
    ord integer NOT NULL,
    type_id smallint NOT NULL,
    data_id integer,
    data_id_type "char" NOT NULL,
    data json,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    CONSTRAINT event_ord_check CHECK ((ord > 0)),
    CONSTRAINT event_type_id_check CHECK ((type_id >= 0))
);

COMMENT ON TABLE event IS 'An append-only sequence of modification events applied to an empty project.';

COMMENT ON COLUMN event.ord IS 'Order in which events for a Project are to be applied.';

COMMENT ON COLUMN event.type_id IS 'A number representing the type of event. Defined and maintained in Scala only.';

COMMENT ON COLUMN event.data_id IS 'Meaning depends on event type. Usually the local ID of an object in the Project.';

COMMENT ON COLUMN event.data_id_type IS 'The type of data_id as IDs can be polymorphic.';

COMMENT ON COLUMN event.data IS 'Meaning and format depends on event type.';

CREATE SEQUENCE project_seq
    START WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1;

CREATE TABLE project (
    id bigint DEFAULT nextval('project_seq'::regclass) NOT NULL,
    usr_id bigint NOT NULL,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    init_events smallint NOT NULL,
    CONSTRAINT project_init_events_check CHECK ((init_events >= 0))
);

COMMENT ON COLUMN project.init_events IS 'The number of synthetic events generated by ShipReq (not the user directly) when the project was created.';

CREATE SEQUENCE usr_seq
    START WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1;

CREATE TABLE usr (
    id bigint DEFAULT nextval('usr_seq'::regclass) NOT NULL,
    username character varying,
    email character varying NOT NULL,
    password character varying,
    password_salt character varying,
    password_changed_at timestamp with time zone,
    confirmation_token character varying,
    confirmation_sent_at timestamp with time zone NOT NULL,
    confirmed_at timestamp with time zone,
    reset_password_token character varying,
    reset_password_sent_at timestamp with time zone,
    reset_password_req_count integer DEFAULT 0 NOT NULL,
    login_count integer DEFAULT 0 NOT NULL,
    last_login_at timestamp with time zone,
    last_login_ip character varying,
    roles character varying,
    CONSTRAINT usr_confirmation_invariants CHECK (
CASE
    WHEN (confirmed_at IS NULL) THEN ((username IS NULL) AND (password IS NULL) AND (password_salt IS NULL) AND (password_changed_at IS NULL) AND (confirmation_token IS NOT NULL) AND (reset_password_req_count = 0) AND (login_count = 0))
    ELSE ((username IS NOT NULL) AND (password IS NOT NULL) AND (password_salt IS NOT NULL) AND (password_changed_at IS NOT NULL) AND (confirmation_token IS NULL) AND (confirmed_at >= confirmation_sent_at) AND (reset_password_req_count >= 0) AND (login_count >= 0))
END),
    CONSTRAINT usr_login_meta CHECK (
CASE
    WHEN (login_count = 0) THEN ((last_login_at IS NULL) AND (last_login_ip IS NULL))
    ELSE (last_login_at IS NOT NULL)
END),
    CONSTRAINT usr_reset_password_meta CHECK (
CASE
    WHEN (reset_password_req_count = 0) THEN ((reset_password_token IS NULL) AND (reset_password_sent_at IS NULL))
    ELSE (reset_password_sent_at IS NOT NULL)
END),
    CONSTRAINT usr_roles_check CHECK (((roles IS NULL) OR ((roles)::text ~ '^[a-z]+(,[a-z]+)*$'::text)))
);

CREATE TABLE usrd (
    usr_id bigint NOT NULL,
    name character varying NOT NULL,
    newsletter boolean NOT NULL
);

CREATE VIEW taskman_users_v01 AS
 SELECT usr.id,
    usr.username,
    usr.email,
    usrd.name,
    usrd.newsletter,
    usr.confirmed_at AS joined
   FROM (usr
     LEFT JOIN usrd ON ((usr.id = usrd.usr_id)))
  WHERE (usr.username IS NOT NULL);

