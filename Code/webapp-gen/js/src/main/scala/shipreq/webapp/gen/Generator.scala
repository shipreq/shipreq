package shipreq.webapp.gen

import japgolly.scalajs.react._
import japgolly.scalajs.react.vdom.VdomElement
import scala.scalajs.LinkingInfo.productionMode
import scala.scalajs.js.JSON
import shipreq.webapp.base.AssetManifest

case class Scala(content: String) extends AnyVal

class Generator[Input](val name: String, val comp: Input => VdomElement, val data: MainAndTest[Input]) {

  private implicit class HtmlOps(html: Html) {
    def toScala: Scala = {
      var expr = JSON.stringify(html.value)
      val q = expr.head
      def tokenise(from: String, to: String) =
        expr = expr.replace(from, s"$q + $to + $q")
      tokenise(AssetManifest.shipreqCircleDarkSvg, "AssetManifest.shipreqCircleDarkSvg")
      Scala(s"Html($expr)")
    }
  }

  def apply(i: Input): Html =
    Generator hack Html(ReactDOMServer.renderToStaticMarkup(comp(i)))

  val html: MainAndTest[Html] =
    data map apply

  val scala: MainAndTest[Scala] =
    html.map(_.toScala)

  val fileContent: Scala =
    Scala(
      s"""
        |package shipreq.webapp.gen.output
        |
        |import shipreq.webapp.base.AssetManifest
        |import shipreq.webapp.gen._
        |
        |// ===============================================================
        |// ===============================================================
        |// ===============================================================
        |//
        |// Generate by running: sbt webapp-gen-js/run
        |//
        |// DO NOT EDIT THIS FILE DIRECTLY
        |//
        |// ===============================================================
        |// ===============================================================
        |// ===============================================================
        |
        |object $name {
        |
        |  val templates: MainAndTest[Html] =
        |    MainAndTest(
        |      ${scala.main.content},
        |      Vector(
        |        ${scala.tests.map(_.content).mkString(",\n\n      ")}))
        |}
      """.stripMargin.trim)

  def printFileContent(): Unit = {
    val sep = "="*120 + "\n"
    println(s"$sep$name\n$sep${fileContent.content}")
  }

  def createFile(): Unit =
    FileApi.write(s"$name.scala", fileContent.content)
}

object Generator {
  def apply[A](name: String, data: MainAndTest[A])(comp: A => VdomElement): Generator[A] =
    new Generator(name, comp, data)

  val hack: Html => Html =
    if (productionMode)
      identity
    else
      // Dev-mode hack to fix asset paths.
      // Release-mode tests will confirm this validity.
      h => Html(h.value.replace("\"/dev/", "\"/a/"))
}
